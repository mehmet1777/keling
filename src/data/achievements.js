// BugÃ¼nÃ¼n tarihini YYYY-MM-DD formatÄ±nda al
export const getTodayDate = () => {
  const today = new Date();
  return today.toISOString().split('T')[0];
};

// BugÃ¼n tekrar edilen farklÄ± koleksiyon sayÄ±sÄ±nÄ± hesapla
export const getTodayRepeatCount = () => {
  const boxProgress = JSON.parse(localStorage.getItem('wordBoxProgress') || '{}');
  const today = getTodayDate();
  let count = 0;
  
  Object.values(boxProgress).forEach(progress => {
    if (progress.completionHistory && progress.completionHistory.length > 0) {
      // BugÃ¼n tamamlanan var mÄ± kontrol et
      const todayCompletion = progress.completionHistory.find(date => 
        date.startsWith(today)
      );
      if (todayCompletion) {
        count++;
      }
    }
  });
  
  return count;
};

// GÃ¼nlÃ¼k tekrar gÃ¶revlerinin durumunu kontrol et
export const checkDailyRepeatQuests = (claimedToday = []) => {
  const todayCount = getTodayRepeatCount();
  
  return dailyRepeatQuests.map(quest => ({
    ...quest,
    current: Math.min(todayCount, quest.target),
    completed: todayCount >= quest.target,
    claimed: claimedToday.includes(quest.id),
    canClaim: todayCount >= quest.target && !claimedToday.includes(quest.id)
  }));
};

// Ã–nerilen koleksiyonlarÄ± bul (en az tekrar edilenler)
export const getSuggestedCollections = (boxProgress, allBoxes) => {
  if (!allBoxes || allBoxes.length === 0) return [];
  
  // Her koleksiyonun tekrar sayÄ±sÄ±nÄ± hesapla
  const collectionsWithCount = allBoxes.map(box => {
    const progress = boxProgress[box.id] || {};
    const repeatCount = progress.completionHistory ? progress.completionHistory.length : 0;
    return {
      ...box,
      repeatCount
    };
  });
  
  // En az tekrar sayÄ±sÄ±nÄ± bul
  const minRepeat = Math.min(...collectionsWithCount.map(c => c.repeatCount));
  const maxRepeat = Math.max(...collectionsWithCount.map(c => c.repeatCount));
  
  // EÄŸer tÃ¼m koleksiyonlar eÅŸit tekrar edilmiÅŸse, rastgele 3 koleksiyon Ã¶ner
  if (minRepeat === maxRepeat) {
    // Rastgele karÄ±ÅŸtÄ±r ve ilk 3'Ã¼ al
    const shuffled = [...collectionsWithCount].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, 3);
  }
  
  // En az tekrar edilenleri bul ve 3 tanesini seÃ§
  const suggested = collectionsWithCount
    .filter(c => c.repeatCount === minRepeat)
    .slice(0, 3);
  
  return suggested;
};
